// This is a simple program to calculate the area of a rectangle

// Constants
const length = 10; // length of the rectangle
const width = 5;   // width of the rectangle

// Variables
let area; // declaring a variable to store the area

// Arithmetic operation to calculate the area
area = length * width; // using the assignment operator

// Output the result
console.log("The area of the rectangle is: " + area); // concatenating strings with the area value

// Data types demonstration
let isRectangle = true; // boolean data type
let description = "This rectangle is a basic shape."; // string data type

// Logging additional information
console.log(description);
console.log("Is it a rectangle? " + isRectangle);
